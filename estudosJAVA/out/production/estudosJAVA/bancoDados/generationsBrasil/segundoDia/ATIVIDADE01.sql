CREATE DATABASE GENERATION_GAME;
USE GENERATION_GAME;


CREATE TABLE CLASSE(
	ID BIGINT AUTO_INCREMENT,
	CLASSE VARCHAR(255),
    PRIMARY KEY(ID)
);

CREATE TABLE PERSONAGEM (
	ID BIGINT AUTO_INCREMENT,
    NOME VARCHAR(255),
    DRI INT,
    DEF INT,
    PAS INT,
    POSICAO BIGINT,
    PRIMARY KEY(ID),
    FOREIGN KEY(POSICAO) REFERENCES CLASSE(ID)
);

