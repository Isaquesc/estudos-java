CREATE DATABASE GENERATION_PIZZARIA;
USE GENERATION_PIZZARIA;

CREATE TABLE CATEGORIA(
	ID BIGINT AUTO_INCREMENT,
	CUSTO_BENEFICIO VARCHAR(255) NOT NULL,
    PRIMARY KEY(ID)
);

CREATE TABLE PIZZA (
	ID BIGINT AUTO_INCREMENT,
    NOME VARCHAR(255) NOT NULL,
    VALOR DECIMAL(6,2) NOT NULL,
	CUSTO BIGINT,
    PRIMARY KEY(ID),
    FOREIGN KEY(CUSTO) REFERENCES CATEGORIA(ID)
);

